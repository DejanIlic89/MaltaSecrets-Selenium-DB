public class AdvertisingDomen {
    
    private int advertise_id;
    private String title;
    private String description;
    private int type;
    private int location;
    private int interest_category_id;
    private int company_id;
    private String image_path;
    private String link;

    public AdvertisingDomen() {
    }

    public int getAdvertise_id() {
        return advertise_id;
    }

    public void setAdvertise_id(int advertise_id) {
        this.advertise_id = advertise_id;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }

    public int getType() {
        return type;
    }

    public void setType(int type) {
        this.type = type;
    }

    public int getLocation() {
        return location;
    }

    public void setLocation(int location) {
        this.location = location;
    }

    public int getInterest_category_id() {
        return interest_category_id;
    }

    public void setInterest_category_id(int interest_category_id) {
        this.interest_category_id = interest_category_id;
    }

    public int getCompany_id() {
        return company_id;
    }

    public void setCompany_id(int company_id) {
        this.company_id = company_id;
    }

    public String getImage_path() {
        return image_path;
    }

    public void setImage_path(String image_path) {
        this.image_path = image_path;
    }

    public String getLink() {
        return link;
    }

    public void setLink(String link) {
        this.link = link;
    }
    
    
}
